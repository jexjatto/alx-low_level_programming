/**
* _abs - prints the absolute value of an integers
*
* Description: this function will print the absolute value
* of the integer.
* @b: b is an integer.
*
* Return: This returns integer
*/
int _abs(int b)
{

	if (b < 0)
	{
		return (-b);
	}
	else if (b == 0)
	{
		return (b);
	}
	else
	{
		return (b);
	}
}
